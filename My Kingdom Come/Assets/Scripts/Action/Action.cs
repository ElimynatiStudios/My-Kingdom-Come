using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Action {

	//================================================================================================
	//[Action Variables]//
	//================================================================================================
	protected Player player;
	protected string actionName;
	protected float actionLength;
	protected Texture2D actionImage;
	protected bool isQueueable;
	//================================================================================================



	//================================================================================================
	//[Constructor]// ---
	//================================================================================================
	public Action(Player player, string actionName, float actionLength, Texture2D actionImage, bool isQueueable){
		this.player = player;
		this.actionName = actionName;
		this.actionLength = actionLength;
		this.actionImage = actionImage;
		this.isQueueable = isQueueable;
	}
	//================================================================================================


	//================================================================================================
	//[GetActionName]// ---
	//================================================================================================
	public string GetActionName(){
		return actionName;
	}
	//================================================================================================



	//================================================================================================
	//[GetActionLength]// ---
	//================================================================================================
	public float GetActionLength(){
		return actionLength;
	}
	//================================================================================================



	//================================================================================================
	//[GetActionImage]// ---
	//================================================================================================
	public Texture2D GetActionImage(){
		return actionImage;
	}
	//================================================================================================



	//================================================================================================
	//[GetIsQueueable]// ---
	//================================================================================================
	public bool GetIsQueueable(){
		return isQueueable;
	}
	//================================================================================================



	//================================================================================================
	//[PerformAction]// ---
	//================================================================================================
	public virtual void PerformAction(){
		
	}
	//================================================================================================


}
